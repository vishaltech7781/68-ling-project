// Google Maps link using coordinates
function getGoogleMapsLink(lat, lng) {
  return `https://www.google.com/maps?q=${lat},${lng}`;
}




// Initialize the application
document.addEventListener('DOMContentLoaded', function() {
    initializeNavigation();
    initializeDirectory();
});

// Navigation functionality
function initializeNavigation() {
    const navLinks = document.querySelectorAll('.nav-link');
    const sections = document.querySelectorAll('.section');

    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href').substring(1);

            // Update active nav link
            navLinks.forEach(l => l.classList.remove('active'));
            this.classList.add('active');

            // Show target section
            sections.forEach(s => s.classList.remove('active'));
            const targetSection = document.getElementById(targetId);
            targetSection.classList.add('active');

            // Scroll to top of section smoothly
            setTimeout(() => {
                targetSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }, 100);
        });
    });
}

// Show lingam details in modal
function showLingamDetails(lingamOrId) {
    let lingam;
    if (typeof lingamOrId === 'number') {
        lingam = lingamData.find(l => l.id === lingamOrId);
    } else {
        lingam = lingamOrId;
    }

    if (!lingam) return;

    const modal = document.getElementById('lingam-modal');
    const modalBody = document.getElementById('modal-body');
    const mapsLink = getGoogleMapsLink(lingam.lat, lingam.lng);

    modalBody.innerHTML = `
        <h3>${lingam.name}</h3>
        <p><strong>Lingam Number:</strong> ${lingam.id}</p>
        <p><strong>Location:</strong> ${lingam.location}</p>
        <p><strong>Area:</strong> ${lingam.area}</p>
        <p>This sacred lingam is part of the 68 Ling Yatra in Solapur. Visit this holy site to seek blessings and complete your spiritual journey.</p>
        <div style="margin-top: 1.5rem;">
            <a href="${mapsLink}" target="_blank" class="btn btn-primary" style="text-decoration: none; display: inline-block;">📍 Open in Google Maps</a>
        </div>
    `;

    modal.classList.add('active');
}

// Close modal
document.addEventListener('click', function(e) {
    const modal = document.getElementById('lingam-modal');
    if (e.target.classList.contains('close') || e.target === modal) {
        modal.classList.remove('active');
    }
});

// Initialize directory with all 68 lingams
function initializeDirectory() {
    renderLingamCards(lingamData);
}

// Render lingam cards
function renderLingamCards(lingams) {
    const container = document.getElementById('lingam-list');
    if (!container) return;
    
    container.innerHTML = '';

    if (lingams.length === 0) {
        container.innerHTML = '<p class="loading">No lingams found.</p>';
        return;
    }

    lingams.forEach(lingam => {
        const mapsLink = getGoogleMapsLink(lingam.lat, lingam.lng);
        const card = document.createElement('div');
        card.className = 'lingam-card';
        card.innerHTML = `
            <span class="lingam-number">Lingam #${lingam.id}</span>
            <h3 class="lingam-name">${lingam.name}</h3>
            <p class="lingam-location">
                <a href="${mapsLink}" target="_blank" class="location-link" title="Click to open in Google Maps">
                    📍 ${lingam.location}
                </a>
            </p>
            <button class="btn btn-primary" onclick="showLingamDetails(${lingam.id})">View Details</button>
            <a href="${mapsLink}" target="_blank" class="btn btn-secondary" style="margin-top: 0.5rem; text-decoration: none; display: inline-block; width: 100%; text-align: center;">Open in Google Maps</a>
        `;
        container.appendChild(card);
    });
}

// Make functions available globally for onclick handlers
window.showLingamDetails = showLingamDetails;
